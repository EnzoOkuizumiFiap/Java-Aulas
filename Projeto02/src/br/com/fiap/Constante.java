package br.com.fiap;

public class Constante {
    public static void main(String[] args) {
        // Declarando e inicializando constantes
        final double PI = 3.1415;
        final float GRAVIDADE_TERRESTRE = 9.8f;

        // Sequência de Escape: \n (Pula Linha)
        // Sequência de Escape: \t (Tabulação)
        System.out.println("O valor de PI:\t\t" + PI + "\nGravidade da Terra:\t" + GRAVIDADE_TERRESTRE + "N");
    }
}
